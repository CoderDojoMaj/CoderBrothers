function onLogin() {
  document.getElementById("logIn").style.display = "block";
}

function onSignIn() {
  document.getElementById("signUp").style.display = "block";
}
  
function offLogin() {
  document.getElementById("logIn").style.display = "none";
}

function offSignIn() {
  document.getElementById("signUp").style.display = "none";
}